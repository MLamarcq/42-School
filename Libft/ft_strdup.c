#include "libft.h"

char	*ft_strdup(const char *s)
{
	char	*dest;
	int		i;
	size_t	j;

	j = ft_strlen(s);
	dest = (char *)malloc(sizeof(char) * (j + 1));
	if (!dest)
	{
		return (NULL);
	}
	i = 0;
	while (s[i] != '\0')
	{
		dest[i] = s[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
